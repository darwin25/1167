		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<Edge.SVA.Model.OtherAccount> DataTableToList(DataTable dt)
		{
			List<Edge.SVA.Model.OtherAccount> modelList = new List<Edge.SVA.Model.OtherAccount>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Edge.SVA.Model.OtherAccount model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = new Edge.SVA.Model.OtherAccount();
					if(dt.Rows[n]["KeyID"]!=null && dt.Rows[n]["KeyID"].ToString()!="")
					{
						model.KeyID=int.Parse(dt.Rows[n]["KeyID"].ToString());
					}
					if(dt.Rows[n]["BankID"]!=null && dt.Rows[n]["BankID"].ToString()!="")
					{
						model.BankID=int.Parse(dt.Rows[n]["BankID"].ToString());
					}
					if(dt.Rows[n]["AccountCode"]!=null && dt.Rows[n]["AccountCode"].ToString()!="")
					{
					model.AccountCode=dt.Rows[n]["AccountCode"].ToString();
					}
					if(dt.Rows[n]["AccountNumber"]!=null && dt.Rows[n]["AccountNumber"].ToString()!="")
					{
					model.AccountNumber=dt.Rows[n]["AccountNumber"].ToString();
					}
					if(dt.Rows[n]["CreatedOn"]!=null && dt.Rows[n]["CreatedOn"].ToString()!="")
					{
						model.CreatedOn=DateTime.Parse(dt.Rows[n]["CreatedOn"].ToString());
					}
					if(dt.Rows[n]["CreatedBy"]!=null && dt.Rows[n]["CreatedBy"].ToString()!="")
					{
						model.CreatedBy=int.Parse(dt.Rows[n]["CreatedBy"].ToString());
					}
					if(dt.Rows[n]["UpdatedOn"]!=null && dt.Rows[n]["UpdatedOn"].ToString()!="")
					{
						model.UpdatedOn=DateTime.Parse(dt.Rows[n]["UpdatedOn"].ToString());
					}
					if(dt.Rows[n]["UpdatedBy"]!=null && dt.Rows[n]["UpdatedBy"].ToString()!="")
					{
						model.UpdatedBy=int.Parse(dt.Rows[n]["UpdatedBy"].ToString());
					}
					modelList.Add(model);
				}
			}
			return modelList;
		}

