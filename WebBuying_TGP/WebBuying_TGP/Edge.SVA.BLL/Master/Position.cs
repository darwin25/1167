		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<Edge.SVA.Model.Position> DataTableToList(DataTable dt)
		{
			List<Edge.SVA.Model.Position> modelList = new List<Edge.SVA.Model.Position>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Edge.SVA.Model.Position model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = new Edge.SVA.Model.Position();
					if(dt.Rows[n]["ID"]!=null && dt.Rows[n]["ID"].ToString()!="")
					{
						model.ID=int.Parse(dt.Rows[n]["ID"].ToString());
					}
					if(dt.Rows[n]["NameSN1"]!=null && dt.Rows[n]["NameSN1"].ToString()!="")
					{
					model.NameSN1=dt.Rows[n]["NameSN1"].ToString();
					}
					if(dt.Rows[n]["NameSN2"]!=null && dt.Rows[n]["NameSN2"].ToString()!="")
					{
					model.NameSN2=dt.Rows[n]["NameSN2"].ToString();
					}
					if(dt.Rows[n]["NameSN3"]!=null && dt.Rows[n]["NameSN3"].ToString()!="")
					{
					model.NameSN3=dt.Rows[n]["NameSN3"].ToString();
					}
					modelList.Add(model);
				}
			}
			return modelList;
		}

